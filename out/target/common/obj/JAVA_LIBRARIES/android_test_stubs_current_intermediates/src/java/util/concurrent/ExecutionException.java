package java.util.concurrent;
public class ExecutionException
  extends java.lang.Exception
{
protected  ExecutionException() { throw new RuntimeException("Stub!"); }
protected  ExecutionException(java.lang.String message) { throw new RuntimeException("Stub!"); }
public  ExecutionException(java.lang.String message, java.lang.Throwable cause) { throw new RuntimeException("Stub!"); }
public  ExecutionException(java.lang.Throwable cause) { throw new RuntimeException("Stub!"); }
}
